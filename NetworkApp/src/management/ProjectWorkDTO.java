package management;

public class ProjectWorkDTO {
	private int projectworkdiary_idx;
	private int yy;
	private int mm;
	private int dd;
	private String content;
	private String icon;
	
	public int getProjectworkdiary_idx() {
		return projectworkdiary_idx;
	}
	public void setProjectworkdiary_idx(int projectworkdiary_idx) {
		this.projectworkdiary_idx = projectworkdiary_idx;
	}
	public int getYy() {
		return yy;
	}
	public void setYy(int yy) {
		this.yy = yy;
	}
	public int getMm() {
		return mm;
	}
	public void setMm(int mm) {
		this.mm = mm;
	}
	public int getDd() {
		return dd;
	}
	public void setDd(int dd) {
		this.dd = dd;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getIcon() {
		return icon;
	}
	public void setIcon(String icon) {
		this.icon = icon;
	}
	
}
